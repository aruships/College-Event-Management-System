    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Connection conn = null;
         try{
             String url1 = "jdbc:mysql://localhost:3306/emsdb";
             String user = "root";
             String password = "0925arushi";
             
             conn = DriverManager.getConnection(url1, user, password);
            if (conn != null) {
                System.out.println("Connected to the database");
                String sqli;
                String R_EV_ID=revidtxt.getText();
                
                 String STAR= rtxt.getText();
                 String R_USN=rusntxt.getText();
               
                PreparedStatement ps;
                sqli = "Insert into emsdb.rating values(?,?,?)";
                ps = conn.prepareStatement(sqli);
               
                ps.setString(1,R_EV_ID );
                ps.setString(2, STAR);
                ps.setString(3,R_USN);
              
                
               
                ps.execute();
                JOptionPane.showMessageDialog(null,"Thanyou for your feedback!");
                conn.close();
                revidtxt.setText(null);
                rtxt.setText(null);
                rusntxt.setText(null);
               
                
                
                
                
            }
         }
 
        
         catch (SQLException ex) {
              if(ex.getErrorCode()==1366)
        {
            JOptionPane.showMessageDialog(null,"please fill the details","ratings failed",2);
            
        }
               if (ex.getErrorCode() == 1452)
               {
                   JOptionPane.showMessageDialog(null,"incorrect details");
               }
               if (ex.getErrorCode() ==1062){
              JOptionPane.showMessageDialog(null,"you have already rated for this event, thankyou!");
               }
               revidtxt.setText(null);
                rtxt.setText(null);
                rusntxt.setText(null);
               
               System.out.println("An error occurred");
            ex.printStackTrace();
        }
    
         finally {
            if (conn != null) {
                try {
                    conn.close();
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
            }
         }
    
    }//GEN-LAST:event_jButton3ActionPerformed

